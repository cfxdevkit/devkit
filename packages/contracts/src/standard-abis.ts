/**
 * Standard token and DeFi interface ABIs for use with viem / wagmi.
 *
 * Two tiers per standard:
 *  - Base (`erc20Abi`, `erc721Abi`, `erc1155Abi`) — the canonical EIP
 *    specification surface only.  Safe to use against any compliant token.
 *  - Extended (`erc20ExtendedAbi`, `erc721ExtendedAbi`) — base plus the
 *    OpenZeppelin 5.x extension functions (mint, burn, pause, permit, etc.)
 *    These match the interface of ERC20Base and ERC721Base from
 *    `@cfxdevkit/contracts`.
 *
 * All ABIs are `as const` for full TypeScript inference with viem.
 */

